size_t base64_decode(const char *src, size_t src_len, uint8_t *dst)
{
    if (src_len == 0)
        return 0;
    if (src_len % 4 != 0)
        return (size_t)-1;

    size_t j = 0;

    for (size_t i = 0; i < src_len; i += 4) {
        uint8_t a = decode_table[(unsigned char)src[i]];
        uint8_t b = decode_table[(unsigned char)src[i + 1]];
        uint8_t c = decode_table[(unsigned char)src[i + 2]];
        uint8_t d = decode_table[(unsigned char)src[i + 3]];

        // b cannot be padding; only positions 2 and 3 allow '='.
        if (a == 0xFF || b == 0xFF || b == 0x40)
            return (size_t)-1;

        int c_pad = (c == 0x40);
        int d_pad = (d == 0x40);

        // "==" is valid only at the very end; "=X" is never valid.
        if (c_pad && !d_pad)
            return (size_t)-1;
        if (i + 4 < src_len && (c_pad || d_pad))
            return (size_t)-1;
        if ((c == 0xFF && !c_pad) || (d == 0xFF && !d_pad))
            return (size_t)-1;

        uint32_t triple = ((uint32_t)a << 18)
                        | ((uint32_t)b << 12)
                        | ((uint32_t)(c_pad ? 0 : c) << 6)
                        |  (uint32_t)(d_pad ? 0 : d);

        dst[j++] = (triple >> 16) & 0xFF;
        if (!c_pad)
            dst[j++] = (triple >> 8) & 0xFF;
        if (!d_pad)
            dst[j++] = triple & 0xFF;
    }

    return j;
}
